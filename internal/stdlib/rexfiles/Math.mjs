// Math.mjs — JS companion for Std:Math

export function toFloat(n) {
    return n;
}

export function round(n) {
    return Math.round(n);
}

export function floor(n) {
    return Math.floor(n);
}

export function ceiling(n) {
    return Math.ceil(n);
}

